package com.philips.model;

public class ACEntity {
	private String aceType;
	private String identifier;
	private String aceFlags;
	private String aceMask;

	public String getAceType() {
		return aceType;
	}

	public void setAceType(String aceType) {
		this.aceType = aceType;
	}

	public String getIdentifier() {
		return identifier;
	}

	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}

	public String getAceFlags() {
		return aceFlags;
	}

	public void setAceFlags(String aceFlags) {
		this.aceFlags = aceFlags;
	}

	public String getAceMask() {
		return aceMask;
	}

	public void setAceMask(String aceMask) {
		this.aceMask = aceMask;
	}

}
